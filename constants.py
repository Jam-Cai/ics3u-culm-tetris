class Game_piece:
  """Class for the active Tetrimino object, holds states, color, and position of each tetrimino"""
  def __init__(self):
    self.block = random.choice(list(shapes.keys()))
    # Shape Name, single character
    self.shape = shapes[self.block]
    
    # Rotation state, depends on shape
    self.r_state = 0
    self.num_r_state = len(self.shape)

    # RGB Value (Red, Green, Blue)
    self.color = shape_colors[self.block]
    
    # Position
    self.x = 5
    self.y = 0
